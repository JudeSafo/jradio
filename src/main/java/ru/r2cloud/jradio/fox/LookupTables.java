	public static float lookup(String table, int rawToFind) {
		Map<Integer, Float> sourceTable = ALL_TABLES.get(table);
		if (sourceTable == null) {
			throw new IllegalArgumentException("unknown table: " + table);
		}
		Float result = sourceTable.get(rawToFind);
		if (result != null) {
			return result;
		}
		float lastValue = 0;
		int lastRaw = 0;
		float prevValue = 0;
		int prevRaw = 0;
		Iterator<Entry<Integer, Float>> it = sourceTable.entrySet().iterator();
		boolean firstKey = true;
		while (it.hasNext()) {
			Entry<Integer, Float> cur = it.next();
			if (firstKey) {
				firstKey = false;
				if (rawToFind < cur.getKey()) {
					if (!it.hasNext()) {
						return ERROR_VALUE;
					}
					Entry<Integer, Float> next = it.next();
					return linearInterpolation(rawToFind, cur.getKey(), next.getKey(), cur.getValue(), next.getValue());
				}
			}
			if (rawToFind < cur.getKey()) {
				return linearInterpolation(rawToFind, lastRaw, cur.getKey(), lastValue, cur.getValue());
			}
			prevRaw = lastRaw;
			prevValue = lastValue;
			lastRaw = cur.getKey();
			lastValue = cur.getValue();
		}
		return linearInterpolation(rawToFind, prevRaw, lastRaw, prevValue, lastValue);
	}

	private static float linearInterpolation(float x, float x0, float x1, float y0, float y1) {
		return y0 + (y1 - y0) * ((x - x0) / (x1 - x0));
	}
